CREATE TABLE MOTO (
                      ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
                      PLACA VARCHAR2(10) NOT NULL,
                      MODELO VARCHAR2(50) NOT NULL,
                      PATIO_ID NUMBER,
                      CONSTRAINT FK_MOTO_PATIO FOREIGN KEY (PATIO_ID) REFERENCES PATIO(ID) ON DELETE SET NULL
);
